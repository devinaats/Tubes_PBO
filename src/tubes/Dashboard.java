    private void fetchDataFromDatabase(String username) {
        try {
            // Establish the database connection
            Connection connection = DriverManager.getConnection(DB_URL, DB_USER, DB_PASS);

            // Prepare the SQL query
            String query = "SELECT * FROM user WHERE usernameUser = ?";
            try (PreparedStatement preparedStatement = connection.prepareStatement(query)) {
                preparedStatement.setString(1, username);
                
                // Execute the query
                try (ResultSet resultSet = preparedStatement.executeQuery()) {
                    if (resultSet.next()) {
                        String userName = resultSet.getString("namaUser");
                        String userEmail = resultSet.getString("emailUser");
                        String userAlamat = resultSet.getString("alamatuser");
                        String userNumber = resultSet.getString("nomorUser");
                        // Populate the GUI components
                        checkNama.setText(userName);
                        fetchNama.setText(userName);
                        fetchEmail.setText(userEmail);
                        fetchAlamat.setText(userAlamat);
                        fetchNomor.setText(userNumber);
                    } else {
                        JOptionPane.showMessageDialog(this, "tidak ada data tersedia");
                    }
                }
            }
            connection.close();
        } catch (SQLException e) {
            e.printStackTrace();
        }
    }
 

