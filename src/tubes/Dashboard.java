    private void editActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_editActionPerformed
         try {
        // Establish the database connection
        Connection connection = DriverManager.getConnection(DB_URL, DB_USER, DB_PASS);
        String username = "lintang1";
        String newNama = fetchNama.getText(); // Assuming these are JTextFields for user input
        String newEmail = fetchEmail.getText();
        String newAlamat = fetchAlamat.getText();
        String newNomor = fetchNomor.getText();
        // Prepare the SQL update query
        String updateQuery = "UPDATE user SET namaUser = ?, emailUser = ?, alamatUser = ?, nomorUser = ? WHERE usernameUser = ?";
        try (PreparedStatement updateStatement = connection.prepareStatement(updateQuery)) {
            updateStatement.setString(1, newNama);
            updateStatement.setString(2, newEmail);
            updateStatement.setString(3, newAlamat);
            updateStatement.setString(4, newNomor);
            updateStatement.setString(5, username);

            // Execute the update query
            int rowsAffected = updateStatement.executeUpdate();

            if (rowsAffected > 0) {
                JOptionPane.showMessageDialog(this, "Profile updated successfully");
                
                // Optionally, you can update the GUI components with the new values
                checkNama.setText(newNama);
                fetchNama.setText(newNama);
                fetchEmail.setText(newEmail);
                fetchAlamat.setText(newAlamat);
                fetchNomor.setText(newNomor);
            } else {
                JOptionPane.showMessageDialog(this, "Failed to update profile");
            }
        }

        connection.close();
    } catch (SQLException e) {
        e.printStackTrace();
    }
        
    }//GEN-LAST:event_editActionPerformed

